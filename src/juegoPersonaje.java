public class juegoPersonaje {
    String nombre;
    String genero;
    String raza;
    String clase;
    int vitalidad;
    int nivel;

    juegoPersonaje(String nombre, String genero, String raza, String clase, int vitalidad, int nivel){
        this.nombre = nombre;
        this.genero = genero;
        this.raza = raza;
        this.clase = clase;
        this.vitalidad = vitalidad;
        this.nivel = nivel;

    }
}
